    public void refrescarTabla(){
        try {
            Vector<Vector> datos = new Vector<>();
            List<IProducto> lista;
            
            lista = RMI.getIProductoController().list();
            
            for( IProducto producto : lista ){
                Vector registro = new Vector();
                registro.add( producto.getProductoId());
                registro.add( producto.getCodigo());
                registro.add( producto.getMarca());
                registro.add( producto.getNombre());
                registro.add( producto.getPrecio());
                
                datos.add( registro );
            }
            
            Vector<String> columnas = new Vector<>();
            columnas.add("ProductoId");
            columnas.add("Codigo");
            columnas.add("Marca");
            columnas.add("Nombre");
            columnas.add("Precio");
            tablaProductos.setModel( new DefaultTableModel(datos, columnas));
        } catch (RemoteException ex) {
            Logger.getLogger(PanelAgregarVenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
